import { json } from '@sveltejs/kit';
import Model from '../Model';

let requiredFieldsForPatch = [];
/** @type {import('./$types').RequestHandler} */
export const PATCH = async ({ request, params }) => {
	try {
		let data = await request.json();

		let { poolId } = params;

		if (!poolId) {
			throw new Error(
				JSON.stringify({
					en: `poolId field is required`,
					tr: `poolId alanı zorunlu`
				})
			);
		}

		if (requiredFieldsForPatch) {
			requiredFieldsForPatch.map((value) => {
				if (!data[value]) {
					throw new Error(
						JSON.stringify({
							en: `${value} field is required`,
							tr: `${value} alanı zorunlu`
						})
					);
				}
			});
		}

		let { name, order, isActive } = data;

		const upsertedPool = await Model.Pool.findByIdAndUpdate(
			poolId,
			{
				name, order, isActive
			},
			{
				upsert: true,
				new: true
			}
		);

		return json({
			status: true,
			pool: upsertedPool
		});
	} catch (error) {
		return json({
			status: false,
			message: error.message
		});
	}
};

/** @type {import('./$types').RequestHandler} */
export const GET = async ({ params }) => {
	console.log(params,"fdfs")
	try {
		let pool = await Model.Pool.findById(params?.poolId);

		return json({
			status: true,
			pool
		});
	} catch (error) {
		return json({
			status: false,
			message: error.message
		});
	}
};

/** @type {import('./$types').RequestHandler} */
export const DELETE = async ({ params }) => {
	try {
		let pool = await Model.Pool.findByIdAndDelete(params?.poolId);

		return json({
			status: true,
			pool
		});
	} catch (error) {
		return json({
			status: false,
			message: error.message
		});
	}
};
